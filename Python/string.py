name = "Reena"  # storing value of string Reena in a variable name
print(name)
print(name + " is a student")  # appending/concatenating a string
print(name.isupper())  # checks if the string contains all
# upper case alphabets.
# Returns True or False accordingly
print(name.islower())  # checks if the string contains all
# lower case alphabets.
print(name.lower())  # all characters of string displayed in lower case alphabets
print(name.upper())  # all characters of string displayed in upper case alphabets
print(name.isupper())
print(name.lower().islower())
''' combining 2 functions.First of all string is
converted into lower case and then is checked'''
print(len(name))  # to find length of string len()
print(name[0])  # gives first character of string i.e R
print(name[1])  # gives second character of string i.e e
print(name[4])  # gives fifth character of string i.e a
print(name.index("n"))
# returns the index where n is present
print(name.replace("Reena", "Sarika"))
